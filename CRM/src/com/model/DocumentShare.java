package com.model;
/**
 * 文档共享实体类
 * @author Administrator
 *
 */
public class DocumentShare implements java.io.Serializable {

	private String id;//编号
	private String type;//类别
	private String title;//标题
	private String uploadDate;//上传日期
	private String uploadPerson;//上传人
	private String url;//地址

	public DocumentShare() {
	}

	public DocumentShare(String id) {
		this.id = id;
	}

	public DocumentShare(String id, String type, String title, String uploadDate, String uploadPerson, String url) {
		this.id = id;
		this.type = type;
		this.title = title;
		this.uploadDate = uploadDate;
		this.uploadPerson = uploadPerson;
		this.url = url;
	}

	public String getId() {
		return this.id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getType() {
		return this.type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getTitle() {
		return this.title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getUploadDate() {
		return this.uploadDate;
	}

	public void setUploadDate(String uploadDate) {
		this.uploadDate = uploadDate;
	}

	public String getUploadPerson() {
		return this.uploadPerson;
	}

	public void setUploadPerson(String uploadPerson) {
		this.uploadPerson = uploadPerson;
	}

	public String getUrl() {
		return this.url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
