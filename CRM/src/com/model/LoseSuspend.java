package com.model;
/**
 * 客户流失暂缓实体类
 */

public class LoseSuspend implements java.io.Serializable {

	private String id;// 编号
	private Customer customer;// 客户编号
	private String postponeDate;// 暂缓时间
	private String postponeMeasure;// 暂缓措施

	public LoseSuspend() {
	}

	public LoseSuspend(String id) {
		this.id = id;
	}

	public LoseSuspend(String id, Customer customer, String postponeDate, String postponeMeasure) {
		this.id = id;
		this.customer = customer;
		this.postponeDate = postponeDate;
		this.postponeMeasure = postponeMeasure;
	}

	public String getId() {
		return this.id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Customer getCustomer() {
		return this.customer;
	}

	public void setCustomer(Customer customer) {
		this.customer = customer;
	}

	public String getPostponeDate() {
		return this.postponeDate;
	}

	public void setPostponeDate(String postponeDate) {
		this.postponeDate = postponeDate;
	}

	public String getPostponeMeasure() {
		return this.postponeMeasure;
	}

	public void setPostponeMeasure(String postponeMeasure) {
		this.postponeMeasure = postponeMeasure;
	}

}
