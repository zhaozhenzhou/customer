package com.model;

/**
 * 服务处理实体类
 * 
 * @author Administrator
 *
 */
public class ServiceHanding implements java.io.Serializable {

	private String id;// 编号
	private Service service;// 服务编号
	private String handing;// 处理内容
	private String handler;// 处理人
	private String handingDate;// 处理时间
	private String handingResult;// 处理结果
	private Integer satisficing;// 满意度1.2.3.4.5 颗星 星级越高满意度越高
	private String archiveDate;// 归档日期

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Service getService() {
		return service;
	}

	public void setService(Service service) {
		this.service = service;
	}

	public String getHanding() {
		return handing;
	}

	public void setHanding(String handing) {
		this.handing = handing;
	}

	public String getHandler() {
		return handler;
	}

	public void setHandler(String handler) {
		this.handler = handler;
	}

	public String getHandingDate() {
		return handingDate;
	}

	public void setHandingDate(String handingDate) {
		this.handingDate = handingDate;
	}

	public String getHandingResult() {
		return handingResult;
	}

	public void setHandingResult(String handingResult) {
		this.handingResult = handingResult;
	}

	public Integer getSatisficing() {
		return satisficing;
	}

	public void setSatisficing(Integer satisficing) {
		this.satisficing = satisficing;
	}

	public String getArchiveDate() {
		return archiveDate;
	}

	public void setArchiveDate(String archiveDate) {
		this.archiveDate = archiveDate;
	}

	public ServiceHanding(String id, Service service, String handing, String handler, String handingDate,
			String handingResult, Integer satisficing, String archiveDate) {
		super();
		this.id = id;
		this.service = service;
		this.handing = handing;
		this.handler = handler;
		this.handingDate = handingDate;
		this.handingResult = handingResult;
		this.satisficing = satisficing;
		this.archiveDate = archiveDate;
	}

	public ServiceHanding() {
		super();
	}

}
