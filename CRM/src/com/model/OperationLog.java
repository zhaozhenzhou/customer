package com.model;
/**
 * 操作日志实体类
 * @author Administrator
 *
 */
public class OperationLog implements java.io.Serializable {

	private String id;// 编号
	private Users users;// 用户编号
	private String operationDate;// 操作时间
	private String firstMenu;// 一级菜单
	private String secondMenu;// 二级菜单
	private String operationContent;// 操作内容

	public OperationLog() {
	}

	public OperationLog(String id) {
		this.id = id;
	}

	public OperationLog(String id, Users users, String operationDate, String firstMenu, String secondMenu,
			String operationContent) {
		this.id = id;
		this.users = users;
		this.operationDate = operationDate;
		this.firstMenu = firstMenu;
		this.secondMenu = secondMenu;
		this.operationContent = operationContent;
	}

	public String getId() {
		return this.id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Users getUsers() {
		return this.users;
	}

	public void setUsers(Users users) {
		this.users = users;
	}

	public String getOperationDate() {
		return this.operationDate;
	}

	public void setOperationDate(String operationDate) {
		this.operationDate = operationDate;
	}

	public String getFirstMenu() {
		return this.firstMenu;
	}

	public void setFirstMenu(String firstMenu) {
		this.firstMenu = firstMenu;
	}

	public String getSecondMenu() {
		return this.secondMenu;
	}

	public void setSecondMenu(String secondMenu) {
		this.secondMenu = secondMenu;
	}

	public String getOperationContent() {
		return this.operationContent;
	}

	public void setOperationContent(String operationContent) {
		this.operationContent = operationContent;
	}

}
